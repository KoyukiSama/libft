#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	src_len;

	src_len = ft_strlen(src);
	if (size == 0)
		return (src_len);
	size--;
	if (size > src_len)
		size = src_len;
	while (size-- && *src)
	{
		*dst++ = *src++;
	}
	*dst = '\0';
	return (src_len);
}
